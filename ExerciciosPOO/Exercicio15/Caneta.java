package ExerciciosPOO.Exercicio15;

public class Caneta {
    private String cor;
    private String marca;

    public Caneta(String cor, String marca) {this.cor = cor; this.marca = marca;}

    public String getCor() {
        return cor;
    }

    public void setCor(String cor) {
        this.cor = cor;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }
}